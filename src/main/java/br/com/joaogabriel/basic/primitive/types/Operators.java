package br.com.joaogabriel.basic.primitive.types;

public class Operators {
    public static void main(String[] args) {
        int firstNumber = 30;
        int secondNumber = 25;

        System.out.println("Values: " + firstNumber + " - " + secondNumber);
        System.out.println("Sum: " + (firstNumber + secondNumber));
        System.out.println("Sub: " + (firstNumber - secondNumber));
        System.out.println("Div: " + (firstNumber / secondNumber));
        System.out.println("Multiply: " + (firstNumber * secondNumber));
        System.out.println("Mod: " + (firstNumber % secondNumber));
    }
}
